export type TranslationKey = keyof typeof translations;

export function getTranslation<T extends TranslationKey>(
  section: T,
  key: keyof typeof translations[T],
  lang: Language
): string {
  const sectionData = translations[section];
  const item = sectionData[key as keyof typeof sectionData];
  
  if (typeof item === "object" && item !== null && lang in item) {
    return (item as unknown as Record<Language, string>)[lang];
  }
  
  return String(item);
}
